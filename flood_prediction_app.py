def calculate_water_level(y0, L, n, dydx, z):
    """
    ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏£‡∏∞‡∏î‡∏±‡∏ö‡∏ô‡πâ‡∏≥‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ‡∏ß‡∏¥‡∏ò‡∏µ‡∏Ç‡∏≠‡∏á‡∏≠‡∏≠‡∏¢‡πÄ‡∏•‡∏≠‡∏£‡πå
    """
    # ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà 1: ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏ä‡πà‡∏ß‡∏á‡∏£‡∏∞‡∏¢‡∏∞‡∏ó‡∏≤‡∏á
    delta_x = L / n
    
    # ‡∏™‡∏£‡πâ‡∏≤‡∏á arrays ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÄ‡∏Å‡πá‡∏ö‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
    x_values = np.linspace(0, L, n + 1)
    y_values = np.zeros(n + 1)
    W_values = np.zeros(n + 1)
    
    # ‡∏Ñ‡πà‡∏≤‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô
    y_values[0] = y0
    W_values[0] = y0 + z
    
    # ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà 2-3: ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì y_i ‡πÅ‡∏•‡∏∞ W_i ‡πÇ‡∏î‡∏¢‡∏ß‡∏¥‡∏ò‡∏µ‡∏Ç‡∏≠‡∏á‡∏≠‡∏≠‡∏¢‡πÄ‡∏•‡∏≠‡∏£‡πå
    for i in range(1, n + 1):
        y_values[i] = y_values[i-1] + dydx * delta_x
        W_values[i] = y_values[i] + z
    
    # ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà 4: ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏£‡∏∞‡∏î‡∏±‡∏ö‡∏ô‡πâ‡∏≥‡πÄ‡∏â‡∏•‡∏µ‡πà‡∏¢
    W_bar = np.mean(W_values)
    
    # ‡∏Ç‡∏±‡πâ‡∏ô‡∏ï‡∏≠‡∏ô‡∏ó‡∏µ‡πà 5: ‡∏Å‡∏≤‡∏£‡∏ï‡∏±‡∏î‡∏™‡∏¥‡∏ô‡πÉ‡∏à‡∏Å‡∏≤‡∏£‡πÄ‡∏Å‡∏¥‡∏î‡∏ô‡πâ‡∏≥‡∏ó‡πà‡∏ß‡∏°
    max_water_level = np.max(W_values)
    flood_risk = max_water_level > z
    
    return x_values, y_values, W_values, W_bar, max_water_level, flood_risk, delta_x

